#addition
add=lambda num1,num2 :num1+num2

print(add(200,200))

#substraction
sub=lambda num1,num2 :num1-num2

print(sub(500,200))

#power of number
pow=lambda num1,num2 :num1**num2

print(pow(2,2))

#cube of number
cube=lambda num:num**3

print(cube(2))

#check given num is even
even=lambda num:num%2==0
print(even(40))



